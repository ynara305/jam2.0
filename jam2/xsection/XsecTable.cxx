void XsecTable::additiveQuarkModel(int kf1,int kf2,double& sig,double& sigel)
{

//...Calculate cross section from additive quark model.
//...Ref;K.Goulianos,Phys.Rep.101,169(1983),H.Sorge,Z.P.C59(1993)85.

    static const double signn=40.0,sigelnn=10.0;
    static const double signn15=pow(signn,1.5);

    int kfa=abs(kf1);
    int kfla=(kfa/1000) %10;
    int kflb=(kfa/100) %10;
    int kflc=(kfa/10) %10;
    if(kfa <= 10 || kfa==21) kflc=kfa;
    int str1=0;
    int nq1=0;
    if(kfla == 3) str1++;
    if(kflb == 3) str1++;
    if(kflc == 3) str1++;
    if(kfla != 0) nq1++;
    if(kflb != 0) nq1++;
    if(kflc != 0) nq1++;
    nq1=max(1,nq1);

    kfa=abs(kf2);
    kfla=(kfa/1000) %10;
    kflb=(kfa/100) %10;
    kflc=(kfa/10) %10;
    if(kfa <= 10 || kfa==21) kflc=kfa;
    int str2=0;
    int nq2=0;
    if(kfla == 3) str2++;
    if(kflb == 3) str2++;
    if(kflc == 3) str2++;
    if(kfla != 0) nq2++;
    if(kflb != 0) nq2++;
    if(kflc != 0) nq2++;
    nq2=max(1,nq2);

    sig=signn*(nq1/3.0)*(nq2/3.0)*(1.0-0.4*str1/nq1)*(1.0-0.4*str2/nq2);
    sigel=pow(sig,1.5)*(sigelnn/signn15);
}

