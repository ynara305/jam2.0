double XsecTable::nnElastic(double srt,int iopt)
{
//...NN elastic cross section from O.Buss et al. Phys. Rep. 512 (2012)1
//...iopt=1: pn,  iopt=2: pp
    static const double  emnuc = 0.93827;
    
    double plab=plabsr(srt,emnuc,emnuc);

//...pn
    if (iopt == 1) {
        if (plab <= 0.525)
          return 17.05*emnuc/(srt*srt-4.0*emnuc*emnuc) - 6.83;
        else if (plab <= 0.8)
          return 33.0 + 196.0*pow(abs(plab-0.95),2.5);
        else if (plab <= 2.0)
          return 31.0/sqrt(plab);
        else
          return  77.0/(plab+1.5);

//...pp/nn
    } else {
        if (plab <= 0.435)
          return 5.12*emnuc/(srt*srt-4.0*emnuc*emnuc) + 1.67;
        else if (plab <= 0.8)
          return  23.5 + 1000.0*pow(plab-0.7,4);
        else if (plab  <= 2.0)
          return 1250.0/(plab+50.0) - 4.0*pow(plab-1.3,2);
        else
          return 77./(plab+1.5);
    }

}

