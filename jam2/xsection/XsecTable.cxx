double XsecTable::pdg2016(double srt,int i)
{
//...fit for high energy cross sections by Regge theory
//...particle data group  2016:
//C. Patrignani et al. (Particle Data Group), Chin. Phys. C, 40, 100001 (2016).
// Ecm > 5 GeV
//...sig_tot= H*log^2(s) + P + R1*s^-eta1 + R2*s^-eta2
//   1: p~   p  total -1: p   p
//   2: p~   n  total -2: p   n
//   3  pi-  p  total -3: pi+ p
//   4: k-   p  total -4: k+  p
//   5: k-   n  total -5: k+  n
//   6: sigma-p total

  static const double M=2.1206;
  static const double H=0.2720;
  static const double eta1 = -0.4473, eta2= -0.5486;

  static const double P[7]= {0, 34.41, 34.71, 18.75, 16.36, 16.31, 34.7};
  static const double R1[7]={0, 13.07,12.52, 9.56, 4.29, 3.70, -46.0};
  static const double R2[7]={0, 7.394,6.66,  1.767, 3.408, 1.826, -48.0};
  static const double m1[7]={0,0.93827,0.93827,0.13957,0.49368,0.49368,1.19745};
  static const double m2[7]={0,0.93827,0.93957,0.93827,0.93827,0.93957,0.93827};

  int j=abs(i);
  double s = srt*srt/pow2(m1[j] + m2[j] + M);
  return H*pow2(log(s)) + P[j] + R1[j]*pow(s,eta1) + i/j*R2[j]*pow(s,eta2);
}
    
